export interface DashboardNote {
  id: string;
  content: string;
  color: 'yellow' | 'blue' | 'green' | 'red' | 'purple' | 'gradient-blue' | 'gradient-purple' | 'gradient-sunset' | 'pattern-dots' | 'pattern-grid';
  type: 'reminder' | 'idea' | 'goal' | 'alert' | 'general' | 'transaction' | 'budget' | 'financial-goal';
  customIcon?: string;
  theme: 'light' | 'dark' | 'auto';
  backgroundImage?: string;
  coverImage?: string;
  emoji?: string;
  createdAt: string;
  updatedAt: string;
  isPinned: boolean;
  isFavorite: boolean;
  linkedTransactionId?: string;
  linkedBudgetId?: string;
  linkedGoalId?: string;
  isAutoGenerated?: boolean;
}

